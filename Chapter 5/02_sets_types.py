s = {1, 2, 3, 4, 5}
e = set()  # Creating an empty set, dont use s = {} as it creates an empty dictionary
print(e)  # This will print the empty set
print(s)  # This will print the original set
print(type(s))  # This will print the type of the variable 's', which is a set
s.add(6)  # Adding an element to the set
print(s)  # This will print the updated set
 